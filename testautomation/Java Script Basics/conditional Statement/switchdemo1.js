/*
    Switch case statement:
    if the input value need to match then we can prefer switch statement
*/
 
let character="Z"
switch(character){
    case "A":
        console.log("A is a Vowel");
        break
    case "E":
        console.log("E is a Vowel");
        break
    case "I":
        console.log("I is a Vowel");
        break
    case "O":
        console.log("O is a Vowel");
        break
    case "U":
        console.log("U is a Vowel");
        break
    default:
        console.log("It is not a Vowel");
       
}
 