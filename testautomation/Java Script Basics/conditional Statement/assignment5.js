// Using nested if else condition
// 3. Print the largest number among given three integer numbers
let a = 1000, b = 344, c = 93737;
if (a > b && a > c) 
    {
    console.log(a + " is the largest number");
} else if (b > a && b > c) 
    {
    console.log(b + " is the largest number");
} else if (c > a && c > b) 
    {
    console.log(c + " is the largest number");
} else 
    {
    console.log("Two or more numbers are equal");
}
