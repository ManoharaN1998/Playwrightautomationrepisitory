/*
  String In-built Functions
*/
// find position of character or word [left to Right]
let s1="xxaxxaxxaXXAXXA"
let v1=s1.indexOf("A")
console.log(v1);

let s2="Lotus is a national flower"
let v2=s2.indexOf("national")
console.log(v2);

// find position of character or word [Right to left]
let s3="xxaxxaxxaXXAXXA"
let v3=s3.lastIndexOf("A")
console.log(v3);

let s4="The sun raises in the east"
let v4=s4.lastIndexOf("in")
console.log(v4);
